### 기본적으로 Phoenix.Presence 는 Authentication 을 통해 생성된 user_token 을 사용하여 추적한다. 그렇다면 guest user 의 경우 Presence 사용이 불가능 한가?


Phoenix.Presence 를 사용하여 현재 접속한 사용자의 활동을 추적할 때, 일반적으로 사용자의 신원을 확인하는 데 user token이나 user_id와 같은 인증 정보를 활용하는 것이 일반적입니다. 인증된 사용자의 신원을 알아야만 특정 사용자의 상태를 정확하게 추적하고 관리할 수 있기 때문입니다.

그러나, 인증 기능을 사용하지 않는 경우, 즉 로그인 없이 접속을 허용하는 경우에도 Phoenix.Presence 기능을 사용할 수 있습니다. 이런 경우에는 다른 방법으로 사용자를 구별할 수 있는 정보를 사용해야 합니다. 예를 들어:

1. 세션 ID 또는 쿠키: 사용자가 웹 사이트에 접속할 때마다 고유한 세션 ID나 쿠키를 생성하여 사용자를 식별할 수 있습니다. 이 정보는 사용자가 로그아웃하거나 세션을 종료할 때까지 유지될 수 있습니다.

2. 임시 식별자: 사용자에게 임시적인 식별자(예: 랜덤으로 생성된 문자열)를 할당하여 사용자의 활동을 추적할 수 있습니다. 이 방법은 사용자가 사이트를 재방문할 때마다 새로운 식별자가 생성될 수 있으므로, 장기적인 사용자 추적에는 적합하지 않을 수 있습니다.

3. IP 주소 또는 기기 정보: 사용자의 IP 주소나 사용하는 기기의 정보를 이용하여 사용자를 구별할 수 있습니다. 이 방법도 완벽하게 정확하지는 않지만, 인증 없이 사용자의 활동을 어느 정도 추적하는 데 사용할 수 있습니다.
